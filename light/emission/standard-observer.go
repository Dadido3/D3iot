// standardObserverCIE represents a standard observer by their color matching functions x̅(λ), y̅(λ), z̅(λ).
type standardObserverCIE struct {
	Start, StepSize float64                     // In nm.
	Table           []struct{ x, y, z float64 } // Table with values of the color matching functions.
}

// Integrate returns the integral of the multiplication of the color matching functions with Φeλ.
//
// Φeλ is the function of the spectral flux.
// Function f has to return the radiant flux Φe, not spectral flux.
// Φe is the integral of the spectral flux in the range [wavelength-bandwidth/2, wavelength+bandwidth/2).
//
// Due to how this is implemented, the CMFs can be seen as interpolated by the nearest neighbor method.
// If you need a more exact result check out IntegrateTrapezoidal.
//
// wavelength and bandwidth are measured in m.
func (s standardObserverCIE) Integrate(f func(wavelength, bandwidth float64) (Φe float64)) (x, y, z float64) {
	stepSize := s.StepSize
	wavelength := s.Start

	for _, entry := range s.Table {
		wavelength += stepSize
		Φe := f(wavelength, stepSize)

		x += entry.x * Φe
		y += entry.y * Φe
		z += entry.z * Φe
	}
	return x, y, z
}

// IntegrateTrapezoidal returns the integral of the multiplication of the color matching functions with the spectral flux Φeλ.
// This uses the trapezoidal rule for numerical integration.
//
// Function f has to return the spectral flux Φeλ for the given wavelength in meters.
func (s standardObserverCIE) IntegrateTrapezoidal(f func(wavelength float64) (Φe float64)) (x, y, z float64) {
	var xMulPrev, yMulPrev, zMulPrev float64

	stepSize := s.StepSize

	wavelength := s.Start
	for i, entry := range s.Table {
		wavelength += stepSize
		Φeλ := f(wavelength)

		xMul, yMul, zMul := entry.x*Φeλ, entry.y*Φeλ, entry.z*Φeλ

		if i > 0 {
			x += (3*xMul - xMulPrev) * stepSize / 2 // Rectangle + Triangle.
			y += (3*yMul - yMulPrev) * stepSize / 2 // Rectangle + Triangle.
			z += (3*zMul - zMulPrev) * stepSize / 2 // Rectangle + Triangle.
		}

		xMulPrev, yMulPrev, zMulPrev = xMul, yMul, zMul
	}
	return x, y, z
}

// ValueLinear returns the value of the three CMFs for the given wavelength.
// The value is interpolated linearly.
//
// The wavelength is measured in m.
func (s standardObserverCIE) ValueLinear(wavelength float64) (x, y, z float64) {
	i := int((wavelength - s.Start) / s.StepSize)

	iWavelength := float64(i)*s.StepSize + s.Start
	rightRatio := iWavelength / s.StepSize
	leftRatio := 1 - rightRatio

	var leftX, rightX, leftY, rightY, leftZ, rightZ float64
	if i >= 0 && i < len(s.Table) {
		leftX, leftY, leftZ = s.Table[i].x, s.Table[i].y, s.Table[i].z
	}
	if i+1 >= 0 && i+1 < len(s.Table) {
		rightX, rightY, rightZ = s.Table[i+1].x, s.Table[i+1].y, s.Table[i+1].z
	}

	return leftX*leftRatio + rightX*rightRatio, leftY*leftRatio + rightY*rightRatio, leftZ*leftRatio + rightZ*rightRatio
}

